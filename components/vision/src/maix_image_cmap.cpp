    std::string cmap_to_str(image::CMap cmap)
    {
        if ((int)cmap < 0 || (int)cmap >= (int)CMap::MAX)
            throw err::Exception(err::ERR_ARGS, "Invalid CMap enum value");
        return _cmap_strs[(int)cmap];
    }

    image::CMap cmap_from_str(const std::string& name)
    {
        std::string lower_name = name;
        std::transform(lower_name.begin(), lower_name.end(), lower_name.begin(), [](unsigned char c){ return std::tolower(c); });

        for (size_t i = 0; i < _cmap_strs.size(); ++i)
        {
            if (_cmap_strs[i] == lower_name)
                return static_cast<CMap>(i);
        }
        throw err::Exception(err::ERR_ARGS, "Invalid colormap name: " + name);
    }

    std::vector<std::string> cmap_strs(bool classify)
    {
        std::vector<std::string> strs;
        for(auto &i : _cmap_strs)
        {
            if(_cmap_data[(int)cmap_from_str(i)]->size() != 256 && !classify)
                continue;
            strs.push_back(i);
        }
        return strs;
    }

    image::Color cmap_color(uint8_t gray, image::CMap cmap)
    {
        auto color = (*_cmap_data[(int)cmap])[gray];
        return image::Color::from_rgb(color[0], color[1], color[2]);
    }

    std::vector<image::Color> cmap_colors(image::CMap cmap)
    {
        auto colors = std::vector<image::Color>();
        colors.reserve(256);
        auto &rgb_colors = *_cmap_data[(int)cmap];
        for (size_t i = 0; i < 256; ++i)
        {
            auto &rgb = rgb_colors[i];
            colors.emplace_back(rgb[0], rgb[1], rgb[2], 1, image::FMT_RGB888);
        }
        return colors;
    }

    const std::vector<std::array<uint8_t, 3>> &cmap_colors_rgb(image::CMap cmap)
    {
        return *_cmap_data[(int)cmap];
    }

